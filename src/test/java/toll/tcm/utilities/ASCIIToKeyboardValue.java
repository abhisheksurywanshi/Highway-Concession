package toll.tcm.utilities;

public class ASCIIToKeyboardValue {

	public static String aSCIIToKeyboardValue(int ascii) {
		if(ascii==112)
		{
			return "F1";
		}
		else if(ascii==113)
		{
			return "F2";
		}
		else if(ascii==114)
		{
			return "F3";
		}
		else if(ascii==115)
		{
			return "F4";
		}
		else if(ascii==116)
		{
			return "F5";
		}
		else if(ascii==117)
		{
			return "F6";
		}
		else if(ascii==118)
		{
			return "F7";
		}
		else if(ascii==119)
		{
			return "F8";
		}
		else if(ascii==120)
		{
			return "F9";
		}
		else if(ascii==121)
		{
			return "F10";
		}
		else if(ascii==122)
		{
			return "F11";
		}
		else if(ascii==123)
		{
			return "F12";
		}
		else 
		{
			return null;
		}
	}

}
